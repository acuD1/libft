#include "libft.h"

long     ft_atol(const char *s)
{
    size_t  i       = 0;
    long    nb      = 0;
    int8_t  sign    = 1;

    while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
        i++;
    if (s[i] == '+' || s[i] == '-')
        sign *= (s[i++] == '-' ? -1 : 1);
    while (s[i] >= '0' && s[i] <= '9')
        nb = (nb * 10) + (s[i++] - '0');
    return (sign * nb);
}
